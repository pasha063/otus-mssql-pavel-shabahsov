USE WideWorldImporters

/*

Это задание из занятия "Операторы CROSS APPLY, PIVOT, UNPIVOT."
Нужно для него написать динамический PIVOT, отображающий результаты по всем клиентам.
Имя клиента указывать полностью из поля CustomerName.

Требуется написать запрос, который в результате своего выполнения 
формирует сводку по количеству покупок в разрезе клиентов и месяцев.
В строках должны быть месяцы (дата начала месяца), в столбцах - клиенты.

Дата должна иметь формат dd.mm.yyyy, например, 25.12.2019.

Пример, как должны выглядеть результаты:
-------------+--------------------+--------------------+----------------+----------------------
InvoiceMonth | Aakriti Byrraju    | Abel Spirlea       | Abel Tatarescu | ... (другие клиенты)
-------------+--------------------+--------------------+----------------+----------------------
01.01.2013   |      3             |        1           |      4         | ...
01.02.2013   |      7             |        3           |      4         | ...
-------------+--------------------+--------------------+----------------+----------------------
*/

declare
	@CustomerName as nvarchar(max)
	,@sql as nvarchar(max)

select @CustomerName = isnull(@CustomerName + ',','') + quotename(CustomerName) from (select CustomerName from Sales.Customers) as CustomerName

set @sql = N'
	select
		InvoiceMonth
		,' + @CustomerName + '
	from (
		select
			format(dateadd(month, datediff(month, 0, i.InvoiceDate), 0), ''dd.MM.yyyy'') as InvoiceMonth
			,c.CustomerName
		from Sales.Invoices as i
			inner join Sales.Customers as c on c.CustomerID = i.CustomerID
	) as unpvt
	pivot (count(CustomerName) for CustomerName in (' + @CustomerName + ')) as pvt
	order by cast(InvoiceMonth as date)
'
exec sp_executesql @sql